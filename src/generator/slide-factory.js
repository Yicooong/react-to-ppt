/**
 * 封面页生成器
 */
function createCoverSlide(pres, componentInfo, config) {
  const { colors, fonts } = config;
  const { name, type, filePath, description = '' } = componentInfo;
  
  const slide = pres.addSlide();
  
  // 背景色
  slide.background = { color: colors.background };
  
  // 标题区域装饰条
  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0.5, y: 1.2, w: 9, h: 0.08,
    fill: { color: colors.primary }
  });
  
  // 组件类型标签
  slide.addText(type, {
    x: 0.5, y: 1.4, w: 2, h: 0.5,
    fontSize: 14, color: colors.primary,
    bold: false, align: 'left', valign: 'middle'
  });
  
  // 主标题
  slide.addText(name, {
    x: 0.5, y: 1.8, w: 9, h: 1.2,
    fontSize: 48, color: colors.text,
    bold: true, fontFace: fonts.title,
    align: 'left', valign: 'middle'
  });
  
  // 文件路径（小字）
  if (filePath) {
    const fileName = filePath.split('/').pop();
    slide.addText(`📁 ${fileName}`, {
      x: 0.5, y: 3.0, w: 4, h: 0.4,
      fontSize: 11, color: '666666',
      align: 'left', valign: 'middle'
    });
  }
  
  // 描述
  if (description) {
    slide.addText(description, {
      x: 0.5, y: 3.6, w: 9, h: 1.0,
      fontSize: 16, color: '666666',
      align: 'left', valign: 'middle'
    });
  }
  
  // 底部信息
  slide.addText(`Generated by React to PPT`, {
    x: 0.5, y: 5.5, w: 8, h: 0.3,
    fontSize: 9, color: '999999',
    align: 'right', valign: 'middle'
  });
  
  return slide;
}

